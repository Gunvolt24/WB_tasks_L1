// ReversedString –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç—Ä–æ–∫—É –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ
func ReversedString(s string) string {
	arr := []rune(s)                                               // –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫—É –≤ —Å—Ä–µ–∑ —Ä—É–Ω –¥–ª—è –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–π —Ä–∞–±–æ—Ç—ã —Å —é–Ω–∏–∫–æ–¥–æ–º
	for i, j := 0, len(arr)-1; i < len(arr)/2; i, j = i+1, j-1 {  // –ú–µ–Ω—è–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–µ—Å—Ç–∞–º–∏, –Ω–∞—á–∏–Ω–∞—è —Å –∫–æ–Ω—Ü–æ–≤ —Å—Ä–µ–∑–∞ –∏ –¥–≤–∏–≥–∞—è—Å—å –∫ —Ü–µ–Ω—Ç—Ä—É
		arr[i], arr[j] = arr[j], arr[i] 								 // –ú–µ–Ω—è–µ–º –º–µ—Å—Ç–∞–º–∏ —ç–ª–µ–º–µ–Ω—Ç—ã —Å –∏–Ω–¥–µ–∫—Å–∞–º–∏ i –∏ j
	}
	return string(arr)
}

func main() {
	s := "–≥–ª–∞–≤—Ä—ã–±–∞ üêü. —Ä–∞–±—Ä—ã–±–∞ üê†, —Ä—ã–±–∞-—ë–∂ üê°, –∞–∫—É–ª–∞ ü¶à, —Å—É—à–∏ üç•"
	fmt.Println(ReversedString(s))
}
