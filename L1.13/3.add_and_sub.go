package main

import "fmt"

// Пример решения при помощи сложения и вычитания.
func main() {
	a, b := 25, 60

	// В переменную a записываем сумму a и b,
	// затем из этой суммы вычитаем b и записываем результат в b,
	// а из новой суммы вычитаем новое значение b и записываем результат в a.
	// В итоге значения a и b меняются местами.
	a = a + b
	b = a - b
	a = a - b

	fmt.Println(a, b)
}
